Enlaces de letras en línea (Letter Links Online) se diseñó para usarse conjuntamente
con el libro <i>Letter Links: Alphabet Learning With Children’s Names</i>, en su
versión en inglés, o en española: <i>Enlaces de letras: Aprendizaje del alfabeto con
los nombres de los niños</i>, en su versión en español. Estos libros de HighScope
Press proporcionan información detallada que le ayudará a entender y utilizar el
sistema de aprendizaje <i>Enlaces de letras</i> (Letter Links) para reforzar el
desarrollo inicial de la lectoescritura en niños pequeños.
</p>
<p>
Indicados para todos los educadores de primera infancia, estos libros parten del
interés natural que los niños tienen por aprender a escribir sus propios nombres.
Este interés se convierte en el punto de partida para enseñarles a reconocer las
letras, la correspondencia letra-sonido y la escritura de letras y palabras. Con
este sistema de aprendizaje de los nombres, cada alumno recibe un enlace de letras
presentado en dos partes: una etiqueta onomástica y una imagen cuyo nombre tiene
la misma letra y mismo sonido iniciales que los del nombre del niño. Empleando este
sistema, los maestros pueden proponer actividades divertidas relacionadas con el
<b>principio alfabético</b>, la <b>conciencia fonológica</b>, la <b>noción de la palabra</b>
y el <b>vocabulario</b>. Las autoras de <i>Enlaces de letras: Aprendizaje del alfabeto
con los nombres de los niños</i> son la Dra. Andrea DeBruin-Parecki, especializada
en lectura y la escritora Mary Hohmann, quien trabaja en el desarrollo de currículos
para la primera infancia, y Christine Snyder.
</p>
<p>
Estos libros contienen numerosos ejemplos de imágenes asociadas a letras que pueden
aprovecharse para representar letras y sonidos en estos idiomas. Enlaces de letras
en línea ofrece las mismas series de imágenes asociadas a letras que se encuentran
en los dos libros. Sin embargo, los libros contienen abundante información adicional
relacionada con las investigaciones sobre la primera fase de la lectoescritura que
han servido de base para el sistema de enlaces de letras. Ambos libros — tanto para
preescolares como para bebés y niños pequeños — ofrecen, además, <b>ocho pasos específicos</b>
a seguir mientras se usa el sistema de enlaces de letras, así como más de 20 actividades
de aprendizaje a partir de enlaces de letras para que los maestros las pongan en
práctica en su salón de clases durante distintas partes del día.
</p>
