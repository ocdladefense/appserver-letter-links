<b>¿Qué son los enlaces de letras?</b>
<p>
El sistema de <i>Enlaces de letras</i> asocia la etiqueta onomástica del niño a
un dibujo de algo cuya palabra tiene como letra y sonido iniciales los mismos que
los del nombre en cuestión. Por ejemplo, el enlace de letras de una <i>Carlota</i>
incluye su nombre y un dibujo de un <i>caballo</i> o una <i>culebra, y</i> la de
un <i>Teodoro</i>, la de su nombre y un dibujo de una <i>tortuga</i> o de una <i>tienda</i>
(de campaña).
</p>
<p>
A partir del vínculo natural que los niños tienen con su propio nombre, el sistema
de enlaces de letras le permite presentar los nombres de las letras y los sonidos
a los niños. A medida que los niños utilicen los enlaces de letras para identificarse
a sí mismos y a sus compañeros, comienzan a adquirir nociones del <i>principio alfabético</i>,
es decir, la comprensión de que existe una relación entre las letras del alfabeto
y los sonidos del lenguaje. Los niños también adquieren una <i>conciencia fonológica</i>,
que se trata de la habilidad de prestar atención a los sonidos lingüísticos independientemente
de su significado.
</p>
