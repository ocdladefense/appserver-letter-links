Los niños y los maestros pueden usar los enlaces de letras de muchas maneras: para
etiquetar el trabajo artístico y los efectos personales de los niños; para identificar
a los niños en los carteles, las listas y los mensajes de la clase, y para crear
actividades de lectoescritura divertidas y entretenidas.
</p>
<p style="display: none;">
Cuando se selecciona el dibujo asociado a la letra de un niño, es importante aprovechar
no solo la misma letra inicial, sino también el mismo sonido. Algunas letras pueden
combinarse con otras. Tomemos como ejemplo la letra <i>B</i>. Usando <i>Enlaces de letras
en línea</i> se ofrecen las combinaciones iniciales <i>B, Bl y Br</i>. Para
el nombre <i>Blanca</i> se escogería una palabra que comienza con <i>Bl</i>, p.
ejem., <i>blanco, blusa, bloque</i>.
</p>
<p>
<i>Enlaces de letras en línea</i> es el único producto de HighScope que proporciona
dibujos tanto para palabras en inglés como para palabras en español. De esta manera,
los maestros que tienen niños hispano-hablantes en su clase cuentan con una mayor
gama de opciones. Por ejemplo, si Juan habla español en casa y aprende inglés en
la escuela, su maestro puede escoger un dibujo con una letra asociada a una palabra
en español como <i>jirafa o jarro</i>.
</p>
<p>
Si quiere más información e ideas sobre cómo usar el sistema de enlaces de letras
en su salón de clases, o sobre las investigaciones y los principios del aprendizaje
de la lectoescritura en las que se basa el sistema, consulte <i>Letter Links: Alphabet
Learning With Children’s Names</i>, de la Dra. Andrea DeBruin-Parecki y Mary
Hohmann, y en la versión en español, <i>Enlaces de letras: Aprendizaje del alfabeto
con los nombres de los niños</i>. Para saber más del crecimiento de la comunicación,
el idioma y la lectoescritura en los bebés y niños pequeños, consulte <i>Infant-Toddler
Letter Links: Children’s Names as Meaningful Environmental Print</i> de Christine
Snyder. Para obtener más información sobre el contenido de estos libros y sobre
cómo pedirlos, haga clic en <b>Recursos</b> en el menú de arriba.
</p>