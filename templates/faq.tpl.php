<b>Adding users</b>
<br />
Click on the MANAGE LICENSES link in the left-hand navigation bar to add users to
your account. Complete the form for each user and assign a unique username (e.g.,
the user’s first initial and last name). Next, assign and confirm the password.
Add any notes that you may want and, finally, click the green <b>Add</b> button
at the bottom of the screen to add the new user. Repeat these steps for each of
the additional licenses you have on your account. A welcome email will automatically
