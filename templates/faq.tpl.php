<span class="faqHeader">Getting Started</span><a name="Started"></a><br />
<br />
<b>What do I do the first time I use Letter Links?</b><br />
Follow these steps:<br />
</p>
<ol>
<li>
<b>Login.</b> The first time you use Letter Links Online, you will log in using
the password assigned to you by email after your purchase of the program license.
After logging in, the licensing agreement will appear on your screen. Please read
the agreement and scroll to the bottom and click the button to agree to the contract.
Next, we recommend that you go to the CHANGE PASSWORD page (click the link in the
left-hand navigation) and create a new password. Then go to MY ACCOUNT and confirm
that your information is correct — if not, please correct it and save. Note that
the program provides instructions as you go along, in both English and Spanish.
<br />
<br />
</li>
<li>
<b>Create a class list.</b> You may then click on CLASS LISTS to get started with
Letter Links. The CLASS LISTS page will allow you to name and select a picture to
identify each group in your class. The program allows you to list the names of multiple
groups since many classrooms have more than one small group or may have more than
one daily session (e.g., Sue’s Morning Group, Sue’s Afternoon Group). Do
not attempt to add students or print the list while you are still on the CLASS LISTS
page — this page is only for entering group names.
<br />
<br />
</li>
<li>
<b>Add students and select pictures.</b> After you have at least one group name
listed on your CLASS LISTS page, it’s time to add students. To do this, click on
the name of the class (e.g., Ernesto’s Class). This will take you to the
STUDENT LIST page. There you can add the names of the students in Ernesto’s group
and select a picture for each student that matches the first sound in the child’s
name. Follow the instructions on the page to select the correct letter sound for
each student and then a corresponding picture. Even if there is only one sound to
choose from, you may still have to click on the sound to make the pictures appear.
You may need to scroll down to see the pictures.
<br />
<br />
</li>
<li>
<b>Print.</b> Now you are ready to print your letter links names or labels. (If
you don't have time to print right away, the program will save the list of students.
If you return later, you will have to click on the group name on the CLASS LISTS
page to return to your list of students.) To print, first scroll down your list
of students on the STUDENT LIST page, and check or uncheck students to be included
in your printout. Then click the navy <b>Print</b> button (not the print command
in your browser). The PRINTING OPTIONS page will come up, showing you all the different
ways you can print your list. Check the options you want, and click the navy <b>Print</b>
button. This will open up a PDF showing your print selection. Next use the printer
icon or the File menu to print the PDF. You can use the print dialog box to select
any options you need, such as printing particular pages or using “fit to paper.”
If you have problems getting the PDF to appear, read the section on printing problems
later in these FAQs.
</li>
</ol>
<p>
Congratulations! You have successfully printed out letter links for your students.
If you have any further problems, note that tips and instructions are available to
guide you throughout the Letter Links site. You can find further help in the remaining
FAQs, or go to the HELP page to contact us.
</p>
