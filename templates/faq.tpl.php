<span class="faqHeader">Printing Letter Links</span><a name="Printing"></a>
<br />
<br />
<b>Okay, I’ve now created my class list of letter links, selected the desired print
options, and hit the Print button. All I get is a blank screen. What should I do?</b>
<br />
Try disabling the pop-up blocker in your web browser (for the Letter Links site
only). You should then get a print preview of your PDF, showing your class list
or letter-linked nametags just as they will be printed. Then hit the <b>Print</b>
control on that screen to print this out.
<br />
<br />
<b>I’ve done the above but no PDF appears. What next?</b>
<br />
Have you downloaded Adobe Reader? You can find it at <a href="https://www.adobe.com">
adobe.com</a>.
<br />
<br />
<b>I still can’t get my list to print. Any more ideas?</b>
<br />
In addition to these common reasons for print failures, occasional printing problems
result from mistakes in using the program, so consider the following:
</p>
<ul>
<li>Are you trying to print from the CLASS LISTS page instead of the STUDENT LIST page?
Note that the CLASS LISTS page is only for naming your class or groups (multiple
groups are okay) and for assigning a picture to identify each group — not for adding
students or printing out the student list, which you must do from the STUDENT LIST
page. After you have named at least one group and selected a picture for it, you
can go to the STUDENT LIST page by clicking on the name of the group on the CLASS
LISTS page. Then follow the instructions to add students and print.
<br />
<br />
</li>
<li>Is there an incomplete name on your student list? If you have accidentally entered
a blank student (no name or picture) or a student with a picture but no name, the
list may fail to print. To correct this, either delete the row or complete the information
(use the <b>Edit Name</b> button).
</li>
</ul>
