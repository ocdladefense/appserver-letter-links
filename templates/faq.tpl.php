<span class="faqHeader">Choosing the Right Letter Sound for a Student’s Name</span><a name="ChoosingName"></a>
<br />
<br />
<b>For some letters, the program only gives you only one choice of letter sound, while
there are a number of choices for other letters. Why?</b>
<br />
The Letter Links system is most effective in promoting beginning reading if the
child’s name is matched with a picture of something that starts not only with the
same letter but also with the same letter sound. Sometimes only one choice of a
letter sound is given for a letter, as with the letter <em>N</em>, because that
letter always stands for the same sound in English. However, some letters can stand
for more than one letter sound, especially when used in combination with other letters.
For example, a student’s name that starts with the letter <em>G</em> may begin with
the hard <em>G</em> or soft <em>G</em> sound, so the program gives selections of
matching pictures for both sounds.
<br />
<br />
<div class="tips">
<div class="tipsTitle">
Tips
</div>
Be sure to read the examples to select the correct sound. For example, <i>acorn</i>,
not <i>alligator</i>, would be a good choice for <i>Abe</i>; for <i>Claire</i>,
you would choose <i>cake</i>, not <i>cymbal</i>.
</div>
<br />
<b>What if I can’t find a letter-linked picture that matches the beginning sound in
a child's name?</b>
<br />
It is not always possible to find appropriate nouns to match the first sound in
some children’s names. For example, the program’s developers could not find a matching
English word (starting with <i>I</i>) for the first sound in the name <i>Ian</i>, which is pronounced
like a long <i>E</i>. In cases like this, one option would be to upload the child’s own
picture to go with his nametag — the <b>Upload</b> feature at the bottom
of the <b>SELECT PICTURE</b> page permits you to do this. Or, for names
of children who are learning English, you might choose a letter-linked word from
the child’s home language. If the child’s name is Spanish in origin, the program
will allow you to upload an image for a Spanish word. For example, for José, you
could select from the Spanish choices and upload a picture of a giraffe for the
Spanish word <em>jirafa</em>. For other languages, you may ask a native speaker
to help you find an appropriate word, and then upload a drawing to create the letter-linked
image.
<br />
<br />
<b>Can I use English and Spanish letter links in the same classroom?</b>
<br />
You do not have to do all your letter links in one language. For example, many teachers
choose to use letter-linked pictures for Spanish words for English language learners
who speak Spanish at home. All the children in the classroom will benefit from the
exposure to letter-sound connections in more than one language.
</p>
