<span class="faqHeader">Uploading Letter-Linked Pictures</span><a name="Uploading"></a>
<br />
<br />
<b>I don’t like (or have used up) the picture choices for a given letter. How do I add
one of my own? </b>
<br />
Choose another word that starts with the same letter sound as the child’s name,
and find or create a drawing for it. Then add the drawing by using the <b>Upload</b>
feature found at the bottom of the <b>SELECT PICTURE</b> page, below the
drawings. Your illustration should be in jpg, gif, or tif format and file size cannot
exceed 4 MB. The program will size the picture automatically and add letters in
the corners of the image.
<br />
<br />
<b>I have a great drawing of a letter-linked picture that I would really like to add to the program.
What should I do?</b>
<br />
You can always use the <b>Upload</b> feature at the bottom of the <b>
SELECT PICTURE</b> page to add the picture to your own class list. If you
are interested in sharing the idea with others, please send us your drawing and/or
word choice at <a href="/cdn-cgi/l/email-protection#99f5fcededfcebf5f0f7f2eaf0f7fff6d9f1f0fef1eafaf6e9fcb7f6ebfe"><span class="__cf_email__" data-cfemail="4c20293838293e202522273f25222a230c24252b243f2f233c2962233e2b">[email&#160;protected]</span></a>.
