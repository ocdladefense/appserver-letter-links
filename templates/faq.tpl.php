<b>Some of the borders of my labels or nametags are getting cut off when I print them.
Ideas?</b>
<br />
When the print dialog box comes up, make sure “Fit to page” is selected, not “No
sizing.”
<br />
<br />
<b>What size labels can I use?</b>
<br />
Labels must be on a 8 1/2″ x 11″ sheet, 14 labels per sheet. Standard label size
is 1 1/3″ x 4″. We recommend using Avery Label 5162; however, compatible Avery labels
may include 15162, 18162, 5162, 5262, 5522, 5962, 8162, 8252, 8462, 8662, 48462,
or 48862. Other brands may work if the dimensions are the same.
<br />
<br />
<b>I’m using the label feature, but the positioning of my labels is a little off. Can
I adjust this?</b>
<br />
The grid for the Avery labels is correctly spaced, but there may be slight differences
in how these will print out on different printers. Small adjustments may be needed.
For best results, print only one sheet of labels at a time. Many teachers find it
useful to print out their labels first on a blank sheet of paper to see how the
grid aligns with the actual labels.
<br />
<br />
<b>When I print out my name labels, one student’s name is too close to the picture or
the end of the name is overlapped by the picture, in one or more label sizes. Can
I fix this?</b>
<br />
This may happen in rare instances. For a quick fix, add a space after the name in
the CLASS LISTS page. Then try printing again. Please also notify us by sending
us a message specifically describing the problem at <a href="/cdn-cgi/l/email-protection#bdd1d8c9c9d8cfd1d4d3d6ced4d3dbd2fdd5d4dad5ceded2cdd893d2cfda">
