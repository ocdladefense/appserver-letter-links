<span class="faqHeader">Creating Class Lists</span><a name="ClassLists"></a>
<br />
<br />
<b>Can I create more than one class list?</b>
<br />
Yes. Although your Letter Links Online license covers just one classroom’s use,
many classrooms have more than one group, so the program allows you to create multiple
lists. For example, you might create one list with all the students’ names and two
shorter lists for small-group activities.
<br />
<br />
<b>Are my class lists private?</b>
<br />
 Your list can only be accessed by you, using the password you have created.
<br />
<br />
<b>Can I download the PDF file listing my students’ names and letter-linked pictures?</b>
<br />
No, your files are stored online. However, you can always save a hard copy of any
list to use for making additional copies.
</p>
